/**
 * generate-math-svg.mjs
 *
 * Converts LaTeX formulas to self-contained SVG path data using MathJax.
 * Output: src/components/multiscale/schematics/mathSvgData.ts
 *
 * Usage: node scripts/generate-math-svg.mjs
 */

import { mathjax } from "mathjax-full/js/mathjax.js";
import { TeX } from "mathjax-full/js/input/tex.js";
import { SVG } from "mathjax-full/js/output/svg.js";
import { liteAdaptor } from "mathjax-full/js/adaptors/liteAdaptor.js";
import { RegisterHTMLHandler } from "mathjax-full/js/handlers/html.js";
import { AllPackages } from "mathjax-full/js/input/tex/AllPackages.js";
import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

// ─── Formula registry ─────────────────────────────────────────────────
// Add new formulas here. Keys become the lookup keys in MathSvg component.

const FORMULAS = [
  // AllAtomSchematic formulas (equation form, display style)
  {
    key: "lj",
    latex: String.raw`\displaystyle E_{\text{LJ}\phantom{\text{ul}}}=4\varepsilon\!\left[\left(\dfrac{\sigma}{r}\right)^{\!12}\!-\left(\dfrac{\sigma}{r}\right)^{\!6}\right]`,
  },
  {
    key: "coulomb",
    latex: String.raw`\displaystyle E_{\text{Coul}}=\dfrac{kq_iq_j}{r}`,
  },
  {
    key: "bond",
    latex: String.raw`\displaystyle E_{\text{bond}}=K_r(r-r_0)^2`,
  },
  {
    key: "angle",
    latex: String.raw`\displaystyle E_{\text{angle}}=K_\theta(\theta-\theta_0)^2`,
  },
  {
    key: "dihedral",
    latex: String.raw`\displaystyle E_{\text{tors}}=\sum\limits_n K_\varphi\![1{+}\cos(n\varphi{-}\gamma)]`,
  },
  // DFTSchematic formulas (for future use)
  {
    key: "vext",
    latex: String.raw`V_{\text{ext}}(\mathbf{r})`,
  },
  {
    key: "rho",
    latex: String.raw`\rho(\mathbf{r})`,
  },
  {
    key: "erho",
    latex: String.raw`E[\rho]`,
  },
  {
    key: "veff",
    latex: String.raw`V_{\text{eff}}[\rho]`,
  },
  {
    key: "phi_eps",
    latex: String.raw`\varphi_i,\;\varepsilon_i`,
  },
  {
    key: "ra_za",
    latex: String.raw`\{R_A, Z_A\}`,
  },
];

// ─── MathJax setup ────────────────────────────────────────────────────

const adaptor = liteAdaptor();
RegisterHTMLHandler(adaptor);

const mathDoc = mathjax.document("", {
  InputJax: new TeX({ packages: AllPackages }),
  OutputJax: new SVG({ fontCache: "none" }), // 'none' = self-contained paths
});

// ─── Convert each formula ─────────────────────────────────────────────

const results = {};

for (const { key, latex } of FORMULAS) {
  const containerNode = mathDoc.convert(latex, { display: false });
  const svgNode = adaptor.firstChild(containerNode);

  const fullSvg = adaptor.serializeXML(svgNode);
  const viewBox = adaptor.getAttribute(svgNode, "viewBox") ?? "0 0 0 0";
  const widthAttr = adaptor.getAttribute(svgNode, "width") ?? "0ex";
  const heightAttr = adaptor.getAttribute(svgNode, "height") ?? "0ex";

  // Extract inner content (strip outer <svg> tags)
  const innerSvg = fullSvg
    .replace(/^<svg[^>]*>/, "")
    .replace(/<\/svg>$/, "");

  // Parse ex values
  const width = parseFloat(widthAttr);
  const height = parseFloat(heightAttr);

  results[key] = { svg: innerSvg, viewBox, width, height };

  console.log(`  ${key}: ${width.toFixed(3)}ex × ${height.toFixed(3)}ex  (${latex.slice(0, 40)}...)`);
}

// ─── Write output ─────────────────────────────────────────────────────

const outPath = resolve(__dirname, "../src/components/multiscale/schematics/mathSvgData.ts");

const tsContent = `// AUTO-GENERATED by scripts/generate-math-svg.mjs — DO NOT EDIT
// Re-generate: node scripts/generate-math-svg.mjs

export interface MathSvgDatum {
  /** Inner SVG content (path/rect/g elements, no outer <svg> wrapper) */
  svg: string;
  /** MathJax viewBox: "minX minY width height" */
  viewBox: string;
  /** Natural width in ex units */
  width: number;
  /** Natural height in ex units */
  height: number;
}

export const MATH_SVG: Record<string, MathSvgDatum> = ${JSON.stringify(results, null, 2)};
`;

writeFileSync(outPath, tsContent, "utf-8");
console.log(`\nWritten to ${outPath}`);
